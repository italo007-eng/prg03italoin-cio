    /**
     * Gera um novo número aleatório entre 1 e 5 usando Math.random()
     * e reinicia a interface
     */
    private void iniciarNovoJogo() {
        // Gera número aleatório: Math.random() retorna valor entre 0.0 e 1.0
        numeroSecreto = (int) (Math.random() * 5) + 1;
        
        lblMensagem.setText("Vou pensar em um valor entre 1 e 5. Tente adivinhar");
        
        // Habilita os controles novamente
        spValor.setEnabled(true);
        btnPalpite.setEnabled(true);
        
        // Define valor padrão no spinner
        spValor.setValue(2);
    }

