    private void btnPalpiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPalpiteActionPerformed
        int palpiteUsuario = (Integer) spValor.getValue();
        
        if (palpiteUsuario == numeroSecreto) {
            lblMensagem.setText("ACERTOU!");
        } else {
            lblMensagem.setText("ERROU! Eu pensei no valor " + numeroSecreto);
        }
        
        // Desabilita os controles após o palpite
        spValor.setEnabled(false);
        btnPalpite.setEnabled(false);
    }//GEN-LAST:event_btnPalpiteActionPerformed

